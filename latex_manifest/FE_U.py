import matplotlib.pyplot as plt
import numpy as np

print("--- КАНОНИЧЕСКИЙ ЯДЕРНЫЙ СИМУЛЯТОР МАНИФЕСТА OSVM ---")
A = np.arange(1, 240, 1)

# Финальные вечные константы, найденные процессором
E0 = 16.67025348  # МэВ (Фундаментальный квант свободной Hex-ячейки)
K_vac = 1.41043628  # МэВ (Модуль объемной упругости каркаса вакуума)
c1 = 0.13092407  # Скорость фрактального разбега деформации
delta_theta = 5.26  # Универсальный угловой люфт соты

# Фрактальная модель кумулятивного угла поверхностного сдвига кластера
theta_surf = 54.735610317 * (1.0 - np.exp(-c1 * (A ** (1 / 3))))

# Спин-изоспиновое упругое натяжение (Паулиевский фильтр решетки)
spin_asymmetry = 0.022 * ((A - 2 * np.round(A / 2)) ** 2) / (A**0.5)

# Уравнение Манифеста OSVM с логарифмическим демпфированием
U_A = E0 * (1.0 - spin_asymmetry) - K_vac * np.log(
    1.0 + (theta_surf - delta_theta) ** 2
)

# Бесшовная калибровка сверхлегких доменов (A < 4)
U_A = np.where(A < 4, U_A * (A / 4), U_A)

# Извлечение чистых скалярных значений (добавлен индекс [0] для исправления ошибки)
fe_val = U_A[np.where(A == 56)]
u_val = U_A[np.where(A == 238)]

print(f"Рекордный пик (Fe-56):    {fe_val[0]:.4f} МэВ (Эталон: 8.7903 МэВ)")
print(f"Рекордный финиш (U-238):  {u_val[0]:.4f} МэВ (Эталон: 7.5701 МэВ)")

# Отрисовка безупречной дуги Астона
plt.figure(figsize=(11, 6))
plt.plot(
    A,
    U_A,
    color="darkred",
    linewidth=2.5,
    label="Логарифмический каркас OSVM",
)
plt.axvline(
    56,
    color="black",
    linestyle="--",
    alpha=0.5,
    label="Точка упругого оптимума (Fe-56)",
)
plt.title(
    "Удельная энергия связи нуклонов через логарифмический Hex-каркас вакуума"
)
plt.xlabel("Массовое число ядра (А) — Количество нуклонов")
plt.ylabel("Удельная энергия связи U(A)/A (МэВ)")
plt.grid(True, linestyle=":", alpha=0.5)
plt.xlim(1, 240)
plt.ylim(0, 10)
plt.legend()
plt.show()
